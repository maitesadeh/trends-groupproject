import Head from "next/head";
import React, { useState } from "react";
import styles from "../styles/Home.module.css";
import Button from "../public/UI/Button";
import Nav from "../public/UI/Nav";
import { app } from "../public/firebase";

import {
  getAuth,
  GoogleAuthProvider,
  signInWithPopup,
  signOut,
} from "firebase/auth";
import { FirebaseError } from "firebase/app";

export default function Home() {
  const [user, setUser] = useState({
    username: "",
    picture: "",
    images: [],
    favorites: [],
  });
  const [isSignedIn, setIsSignedIn] = useState(false);
  const auth = getAuth(app);

  const provider = new GoogleAuthProvider();
  const signInWithGoogle = () => {
    signInWithPopup(auth, provider)
      .then((result) => {
        const name = result.user.displayName + "";
        const profilePic = result.user.photoURL + "";

        setUser({ ...user, username: name, picture: profilePic });

        setIsSignedIn(true);
      })
      .catch((error) => alert(error));
  };

  const signOutFromApp = () => {
    // auth.signOut();
    // signOut(auth).then((res) => console.log(res));
  };

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
      </Head>
      <Nav></Nav>
      <div className={styles.home}>
        <div>
          <h1>Cornell Photography</h1>
          <Button name="Sign In With Google" onClick={signInWithGoogle} />
          {isSignedIn ? <p>Hello {user.username}</p> : <p>Havent signed In</p>}
          {/* <Button name="Sign Out" onClick={signOutFromApp} /> */}
        </div>
      </div>
    </div>
  );
}
